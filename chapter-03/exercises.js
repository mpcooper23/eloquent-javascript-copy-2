function isEven(num) {
//base
if(num === 0){
  return true;
}
//recursion
if (num === 1){
  return false;
}
  // Handle negative numbers by converting them to positive
  if (num < 0) {
    return isEven(-num);
  }
return isEven(num - 2)
}
////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  let count = 0;
  for(let i = 0; i < string.length; i++){
    if(string[i] === char){
      count += 1
    }
  }
  return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
let count = 0;
for(let i = 0; i < string.length; i++){
  if(string[i] === 'B'){
    count += 1
  }
}
return count;
}

