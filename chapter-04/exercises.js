// arrayToList /////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function arrayToList(array) {
let rest = null;
//reverse for loop
for(let i = array.length - 1; i >= 0; i--){
  rest = { value: array[i], rest: rest}; //reassigning rest to an object
}
return rest;
}

